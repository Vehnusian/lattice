	<p class="font-mono text-xs uppercase tracking-wider text-(--color-ink-subtle)">About</p>
	<h1 class="mt-3 text-3xl font-semibold tracking-tight text-(--color-ink)">
		What this is, and what it isn't.
	</h1>

	<section class="mt-10 space-y-5 text-(--color-ink-muted) leading-relaxed">
		<p>
			A reference library of validated complex systems models — segregation, epidemics, networks,
			cellular automata, reaction-diffusion, self-organized criticality, and others.
		</p>
		<p>
			Each page presents one model: an interactive simulation with the parameters that matter, a
			short explanation of how it works, suggested experiments, the underlying math, and citations
			to the original paper and a canonical textbook.
		</p>
		<p>
			Models are validated against known analytic results where they exist, and against a Python
			reference implementation otherwise. Validation results are published in the repository.
		</p>
		<p>
			Code is released under <a href="https://github.com/Vehnusian/lattice/blob/main/LICENSE">MIT</a>;
			prose and figures under
			<a href="https://github.com/Vehnusian/lattice/blob/main/LICENSE-docs">CC-BY-4.0</a>.
		</p>
	</section>

