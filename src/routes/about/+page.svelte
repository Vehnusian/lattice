	<section class="mt-8 space-y-4 text-(--color-text-muted)">
		<p>
			lattice is a reference library of validated complex systems models — segregation, epidemics,
			networks, cellular automata, reaction-diffusion, self-organized criticality, and others.
		</p>
		<p>
			Each page presents one model: an interactive simulation with the parameters that matter, a
			short explanation of how it works, suggested experiments, the underlying math, and
			citations to the original paper and a canonical textbook.
		</p>
		<p>
			Models are validated against known analytic results where they exist, and against a Python
			reference implementation otherwise. Validation results are published in the repository.
		</p>
		<p>
			Code is released under MIT; prose and figures under CC-BY-4.0. The source is on GitHub.
		</p>
